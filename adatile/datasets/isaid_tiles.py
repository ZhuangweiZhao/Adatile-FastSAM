"""
iSAID 预切 Tile 数据集 | Pre-cut iSAID Tile Dataset
=====================================================

读取 prep_isaid_tiles.py 输出的预切瓦片。
Reads pre-cut tiles generated by prep_isaid_tiles.py.

特性 | Features:
    - 直接读 1024×1024 PNG → ~20ms/sample (vs ~5s 在线切 tile)
    - 支持二值 (semantic=False) 和多类别 (semantic=True) 模式
    - 无 COCO 解析、无多边形渲染、无大图加载

用法 | Usage:
    ds = FastISAIDTileDataset(root_dir="data/iSAID_tiles", split="train", semantic=True)
    sample = ds[0]  # {"image": [3, 1024, 1024], "mask": [1024, 1024]}
"""

from __future__ import annotations
from pathlib import Path
import numpy as np
import torch
from PIL import Image
from torch.utils.data import Dataset

from adatile.logging import get_logger

logger = get_logger("isaid_tiles")


class FastISAIDTileDataset(Dataset):
    """
    预切 iSAID Tile 数据集 | Pre-cut iSAID Tile Dataset.

    Parameters
    ----------
    root_dir : str
        预切数据根目录 (prep_isaid_tiles.py 的输出目录).
    split : str
        "train" / "val" / "test".
    semantic : bool
        True = mask 为 [H,W] int64 类别标签 (0=bg, 1-15).
        False = mask 为 [H,W] uint8 0/255 二值.
    """

    def __init__(self, root_dir: str = "data/iSAID_tiles",
                 split: str = "train", semantic: bool = True):
        self.root = Path(root_dir)
        self.split = split
        self.semantic = semantic

        self._img_dir = self.root / "images" / split
        self._mask_dir = self.root / "masks" / split

        # 扫描所有 tile | Scan all tiles
        self._tiles = sorted(
            p.name for p in self._img_dir.glob("*.png")
        )
        if not self._tiles:
            raise FileNotFoundError(
                f"未找到 tile 文件, 请先运行 prep_isaid_tiles.py | "
                f"No tile files found in {self._img_dir}. Run prep_isaid_tiles.py first."
            )

        logger.log_info(
            "dataset/isaid_tiles_init",
            f"FastISAIDTile {split}: {len(self)} tiles, "
            f"semantic={semantic}, avg ~20ms/sample",
        )

    def __len__(self) -> int:
        return len(self._tiles)

    def __getitem__(self, index: int) -> dict:
        fname = self._tiles[index]

        # 图像: cv2 → RGB float32 | Image: cv2 → RGB float32
        try:
            import cv2
            img_bgr = cv2.imread(str(self._img_dir / fname), cv2.IMREAD_COLOR)
            if img_bgr is None:
                raise ValueError(f"Corrupted image: {fname}")
            img_np = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2RGB).astype(np.float32) / 255.0
            # 掩码: cv2 单通道读取 | Mask: cv2 grayscale read
            mask_np = cv2.imread(str(self._mask_dir / fname), cv2.IMREAD_UNCHANGED)
            if mask_np is None:
                raise ValueError(f"Corrupted mask: {fname}")
        except Exception:
            # 损坏文件 → 跳过, 随机选另一个 | Corrupted file → skip, pick another
            logger.log_info("data/skip_corrupted",
                            f"Skipping corrupted tile: {fname}, trying next")
            new_idx = (index + 1) % len(self._tiles)
            return self.__getitem__(new_idx)

        img_t = torch.from_numpy(img_np).permute(2, 0, 1)  # [3, H, W]

        if self.semantic:
            # 类别 ID 0-15 → int64 标签 | Category IDs 0-15 → int64 labels
            mask_t = torch.from_numpy(mask_np.astype(np.int64))  # [H, W] long
        else:
            # 二值模式: >0 即为前景 | Binary mode: >0 = foreground
            mask_t = torch.from_numpy((mask_np > 0).astype(np.float32))  # [H,W] float

        return {
            "image": img_t,
            "mask": mask_t,
            "image_id": fname,
            "image_size": tuple(img_t.shape[1:]),
        }
